var cislo = 5; // číslo, u kterého zjistíme jestli je sudé nebo liché

var vysledek = cislo % 2; // zjistíme celo číselný zbytek čísla, více že když šílo je dělitelné 2 (2,4,8..) tak je sudé a když není (1,3,5..) je liché

console.log(vysledek); // vypíše to 1 nebo 0 podle toho jestli je sudé nebo liché, 0 sudé 1 liché

//v tomto případě je výsledek 1 (sudé). Zase se ke kódu vvrátíme později.
